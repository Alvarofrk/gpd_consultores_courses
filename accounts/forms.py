    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        # Importar Course aquí para evitar importación circular
        self.fields['courses'].queryset = Course.objects.all().order_by('title')
        
        # Establecer valor predeterminado para email solo en GET (no en POST)
        if not self.data:
            self.fields['email'].initial = "sinespecificar@gmail.com"

    def clean_email(self):
        email = self.cleaned_data.get('email')
        
        # Si está vacío o es el valor predeterminado sin cambios, generar email único
        if not email or email.strip() == "" or email.strip() == "sinespecificar@gmail.com":
            from accounts.utils import generate_default_email
            email = generate_default_email()
        
        # Validar unicidad
        if User.objects.filter(email__iexact=email, is_active=True).exists():
            raise forms.ValidationError("Este correo electrónico ya está en uso, por favor utiliza otro.")
        
        return email

    class Meta(UserCreationForm.Meta):
        model = User

    @transaction.atomic()
    def save(self, commit=True):
        user = super().save(commit=False)
        user.is_student = True
        user.first_name = self.cleaned_data.get("first_name")
        user.last_name = self.cleaned_data.get("last_name")
        user.gender = self.cleaned_data.get("gender")
        user.address = self.cleaned_data.get("address")
        user.phone = self.cleaned_data.get("phone")
        user.address = self.cleaned_data.get("address")
        user.email = self.cleaned_data.get("email")

        if commit:
            user.save() 
            student = Student.objects.create(
                student=user,
                level=self.cleaned_data.get("level"),
                cargo=self.cleaned_data.get("cargo"),
                empresa=self.cleaned_data.get("empresa"),
                program=self.cleaned_data.get("program"),
            )
            
            # Asignar los cursos seleccionados
            courses = self.cleaned_data.get("courses")
            if courses:
                student.courses.set(courses)
                
                # Crear registros en TakenCourse para cada curso seleccionado
                from result.models import TakenCourse
                for course in courses:
                    TakenCourse.objects.get_or_create(
                        student=student,
                        course=course,
                        defaults={
                            'grade': None,
                            'comment': 'Curso asignado durante la creación del participante'
                        }
                    )

        return user


